-module(new_lager_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    case new_lager_sup:start_link() of
        {ok, Pid} ->
            application:start(lager),
            {ok, Pid};
        Error ->
            Error
    end.

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

